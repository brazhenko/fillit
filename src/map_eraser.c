void		map_eraser(char ***map, char sign, int sz)
{
	int		i;
	int		j;

	i = 3;
	j = 3;
	while (i < sz + 3)
	{
		while (j < sz + 3)
		{
			if ((*map)[i][j] > sign)
			{
				(*map)[i][j] = '.';
			}
			j += 1;
		}
		j = 0;
		i += 1;
	}
}
